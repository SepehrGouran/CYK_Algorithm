public class Main {

    public static void main(String[] args) {

        int[][] table = new int[5][5];

        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                System.out.print(table[i][j] + "\t");
            }
            System.out.println("");
        }

    }
}
